"""Materialise the A1-A10 ablation configs as standalone TOML files.

The published ablation grid (see results/analysis/phase5_full_750core.md) is a
modality on/off matrix over five sources: price (always on) + {news, social,
macro, graph}. This generator writes one ``experiments/A<N>.toml`` per config so
a reviewer can launch any single cell with::

    python scripts/run_platform.py run --config configs/mmfp/experiments/A4.toml \
        --set data.fold_idx=0 --set seed=42 --output /tmp/a4.csv

Each file overrides ONLY the modality toggles on top of ``defaults.toml``; every
other hyper-parameter comes from the canonical defaults (which were frozen before
any ablation run). The (config -> modality) mapping is transcribed verbatim from
the ablation driver and matches the published tables.

The full ablation in the paper sweeps each config over
{concat, gated, mha} fusion x {feedforward, lstm} price encoder x 5 folds x 3
seeds. These files fix concat + the default encoder; pass --set fusion.strategy /
model.price_encoder / data.fold_idx / seed to cover the rest of the grid.

Run from the artifact root::  python configs/mmfp/_generate_ablation_configs.py
"""
from __future__ import annotations

from pathlib import Path

# config label -> {modality: enabled}. Price is always on.
# Transcribed from the ablation driver; matches the published phase5 tables.
# A1-A9 are the core grid; A10 is the supplementary price+news+social cell.
CONFIGS: dict[str, dict[str, bool]] = {
    "A1": {"news": True,  "social": True,  "macro": True,  "graph": False},  # price + news + social + macro (no graph)
    "A2": {"news": True,  "social": False, "macro": True,  "graph": False},  # price + news + macro
    "A3": {"news": True,  "social": False, "macro": False, "graph": False},  # price + news
    "A4": {"news": False, "social": False, "macro": True,  "graph": False},  # price + macro
    "A5": {"news": False, "social": True,  "macro": True,  "graph": False},  # price + macro + social
    "A6": {"news": False, "social": True,  "macro": False, "graph": False},  # price + social
    "A7": {"news": False, "social": False, "macro": False, "graph": False},  # price only (baseline)
    "A8": {"news": False, "social": False, "macro": False, "graph": True},   # price + graph
    "A9": {"news": False, "social": False, "macro": True,  "graph": True},   # price + macro + graph
    "A10": {"news": True, "social": True,  "macro": False, "graph": False},  # price + news + social (supplementary)
}

DESC = {
    "A1": "price + news + social + macro (full, no graph)",
    "A2": "price + news + macro",
    "A3": "price + news",
    "A4": "price + macro",
    "A5": "price + macro + social",
    "A6": "price + social",
    "A7": "price only (baseline)",
    "A8": "price + graph",
    "A9": "price + macro + graph",
    "A10": "price + news + social (supplementary)",
}

TEMPLATE = """# {label} ablation cell -- {desc}
#
# Generated by configs/mmfp/_generate_ablation_configs.py. Overrides only the
# modality toggles on top of defaults.toml; all hyper-parameters come from the
# frozen canonical defaults. The default price encoder is LSTM and fusion is
# concat; the paper sweeps {{concat, gated, mha}} x {{feedforward, lstm}} x 5 folds
# x 3 seeds. Cover the rest of the grid with --set, e.g.:
#   --set model.price_encoder=feedforward --set fusion.strategy=gated \\
#   --set data.fold_idx=2 --set seed=123
#
# Launch:
#   python scripts/run_platform.py run --config configs/mmfp/experiments/{label}.toml \\
#       --set data.fold_idx=0 --set seed=42 --output /tmp/{label_lower}.csv

name = "{label}"
seed = 42                  # overridden per run

[data]
fold_idx = 0               # overridden per run

[price]
enabled = true

[news]
enabled = {news}

[social]
enabled = {social}

[macro]
enabled = {macro}

[graph]
enabled = {graph}
"""


def _b(x: bool) -> str:
    return "true" if x else "false"


def main() -> None:
    out_dir = Path(__file__).resolve().parent / "experiments"
    out_dir.mkdir(parents=True, exist_ok=True)
    for label, flags in CONFIGS.items():
        text = TEMPLATE.format(
            label=label,
            label_lower=label.lower(),
            desc=DESC[label],
            news=_b(flags["news"]),
            social=_b(flags["social"]),
            macro=_b(flags["macro"]),
            graph=_b(flags["graph"]),
        )
        (out_dir / f"{label}.toml").write_text(text)
        print(f"wrote {out_dir / (label + '.toml')}")


if __name__ == "__main__":
    main()
