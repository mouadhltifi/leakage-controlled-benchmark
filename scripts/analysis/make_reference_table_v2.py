#!/usr/bin/env python3
"""Reference table v2 — every row harness-native, one estimand, honest-unit
statistics (Paper B table-v2 spec, 2026-07-14).

Composition: concat-fusion cells per (fold, seed, arch), all configs paired
against the harness-native concat A7:
  A7, A3, A6  -> results/native_core/   (mmfp, 2026-07-14 campaign)
  A8, A9      -> results/sector/sector_ff12_*.csv (mmfp, FF12 = the shipped
                 reference graph per the sector-equivalence fallback; A9's
                 macro is the publication-lagged cache)
  A4, A2, A5, A1 -> results/macrolag/ (mmfp, 2026-06 campaign), concat rows
                 [swapped to results/native_macro/ automatically if present]

Statistics per config (30 cells = 5 folds x 3 seeds x 2 arch):
  - paired cell deltas vs A7's matching cells; point estimate = mean;
  - fold-block bootstrap 95% CI (resample the 5 folds with replacement,
    10,000 draws, seed 42) -- the honest-unit interval;
  - fold-level test: t-test on the 5 per-fold mean deltas (n=5), Bonferroni
    k=8;
  - pooled-30 t-test reported ONLY as an anti-conservative sensitivity;
  - d = mean/sd(ddof=1) over the 30 cells (descriptive).

Prints the FF12 price+graph vs price verdict prominently (the question the
sector-equivalence doc left open).
"""
from __future__ import annotations

import argparse
from pathlib import Path

import numpy as np
import pandas as pd
from scipy import stats

ROOT = Path(__file__).resolve().parents[2]
R = ROOT / "results"

CONFIGS = [  # display, config id, block
    ("P+N",     "A3", "native_core"),
    ("P+M",     "A4", "macro"),
    ("P+S",     "A6", "native_core"),
    ("P+G",     "A8", "sector"),
    ("P+N+M",   "A2", "macro"),
    ("P+M+S",   "A5", "macro"),
    ("P+M+G",   "A9", "sector"),
    ("P+N+M+S", "A1", "macro"),
]


def _cells(paths: list[Path], pattern: str, cfg: str) -> pd.Series:
    frames = []
    for p in paths:
        d = pd.read_csv(p)
        arch = "lstm" if "lstm" in p.name else "ff"
        d = d[d.experiment_name.str.contains(pattern.format(cfg=cfg))]
        d["arch"] = arch
        frames.append(d[["fold_idx", "seed", "arch", "mcc"]])
    df = pd.concat(frames, ignore_index=True)
    return df.set_index(["fold_idx", "seed", "arch"]).mcc.sort_index()


def load(cfg: str, block: str) -> tuple[pd.Series, str]:
    native_macro = R / "native_macro"
    if block == "native_core":
        return _cells([R / f"native_core/native_core_{a}.csv" for a in ("ff", "lstm")],
                      r"natcore_{cfg}_", cfg), "mmfp native"
    if block == "sector":
        return _cells([R / f"sector/sector_ff12_{a}.csv" for a in ("ff", "lstm")],
                      r"secff12_{cfg}_(?:ff|lstm)_concat_", cfg), "mmfp native (FF12 graph)"
    if native_macro.exists():
        return _cells([native_macro / f"native_macro_{a}.csv" for a in ("ff", "lstm")],
                      r"natmacro_{cfg}_", cfg), "mmfp native"
    return _cells([R / f"macrolag/ablation_macrolag_{a}.csv" for a in ("ff", "lstm")],
                  r"macrolag_{cfg}_(?:ff|lstm)_concat_", cfg), "mmfp native (Jun run)"


def fold_block_ci(cell_deltas: pd.Series, n_boot: int = 10_000, seed: int = 42):
    rng = np.random.default_rng(seed)
    folds = cell_deltas.index.get_level_values("fold_idx").unique().to_numpy()
    by_fold = {f: cell_deltas.xs(f, level="fold_idx").to_numpy() for f in folds}
    means = np.empty(n_boot)
    for i in range(n_boot):
        pick = rng.choice(folds, size=len(folds), replace=True)
        means[i] = np.concatenate([by_fold[f] for f in pick]).mean()
    return float(np.quantile(means, 0.025)), float(np.quantile(means, 0.975))


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--outdir", default=str(ROOT / "tables"))
    args = ap.parse_args()
    outdir = Path(args.outdir)
    outdir.mkdir(parents=True, exist_ok=True)

    a7, _ = load("A7", "native_core")
    assert len(a7) == 30, f"A7 cells {len(a7)} != 30"

    rows = []
    for disp, cfg, block in CONFIGS:
        c, prov = load(cfg, block)
        idx = c.index.intersection(a7.index)
        assert len(idx) == 30, f"{cfg}: matched {len(idx)} != 30"
        deltas = (c.loc[idx] - a7.loc[idx]).sort_index()
        m = float(deltas.mean())
        sd = float(deltas.std(ddof=1))
        d = m / sd if sd > 0 else 0.0
        lo, hi = fold_block_ci(deltas)
        fold_means = deltas.groupby("fold_idx").mean().to_numpy()
        _, p_fold = stats.ttest_1samp(fold_means, 0.0)
        _, p_pool = stats.ttest_1samp(deltas.to_numpy(), 0.0)
        pb = min(1.0, 8 * float(p_fold))
        rows.append((disp, m, lo, hi, d, float(p_fold), pb, float(p_pool), prov))

    tex = outdir / "ref_direction_v2.tex"
    with tex.open("w") as f:
        f.write("% Generated by scripts/analysis/make_reference_table_v2.py -- "
                "harness-native, concat estimand, fold-level statistics. Do not hand-edit.\n")
        for disp, m, lo, hi, d, pf, pb, pp, prov in rows:
            pbs = f"{pb:.3f}" if pb < 0.9995 else "1.000"
            f.write(f"{disp} & {m:+.4f} & [{lo:+.4f}, {hi:+.4f}] & {d:+.3f} & "
                    f"{pf:.3f} & {pbs} & {prov} \\\\\n")
        f.write("\\bottomrule\n")

    print(f"{'config':9s} {'dMCC':>8s} {'fold-block 95% CI':>20s} {'d':>7s} "
          f"{'p_fold':>7s} {'p_bonf':>7s} {'p_pool(sens)':>12s}  provenance")
    for disp, m, lo, hi, d, pf, pb, pp, prov in rows:
        pbs = f"{pb:.3f}" if pb < 0.9995 else "1.000"
        star = "  <-- check" if (lo > 0 or hi < 0) else ""
        print(f"{disp:9s} {m:+8.4f}  [{lo:+.4f}, {hi:+.4f}] {d:+7.3f} "
              f"{pf:7.3f} {pbs:>7s} {pp:12.3f}  {prov}{star}")
    a8 = [r for r in rows if r[0] == "P+G"][0]
    print(f"\nFF12 P+G vs native price-only: delta={a8[1]:+.4f}, "
          f"CI=[{a8[2]:+.4f},{a8[3]:+.4f}], p_fold={a8[5]:.3f}, p_bonf={a8[6]:.3f} -> "
          f"{'WITHIN the null' if a8[6] > 0.05 and (a8[2] <= 0 <= a8[3]) else 'REQUIRES SCRUTINY'}")
    print(f"wrote {tex}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
